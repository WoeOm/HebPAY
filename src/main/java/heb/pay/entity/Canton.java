package heb.pay.entity;

public class Canton {
	
	private int cantonid;
	private String cantoncode;
	private String cantonname;
	private String upcantoncode;
	private int levels;
	private int isleaf;
	private int isenable;
	public int getCantonid() {
		return cantonid;
	}
	public void setCantonid(int cantonid) {
		this.cantonid = cantonid;
	}
	public String getCantoncode() {
		return cantoncode;
	}
	public void setCantoncode(String cantoncode) {
		this.cantoncode = cantoncode;
	}
	public String getCantonname() {
		return cantonname;
	}
	public void setCantonname(String cantonname) {
		this.cantonname = cantonname;
	}
	public String getUpcantoncode() {
		return upcantoncode;
	}
	public void setUpcantoncode(String upcantoncode) {
		this.upcantoncode = upcantoncode;
	}
	public int getLevels() {
		return levels;
	}
	public void setLevels(int levels) {
		this.levels = levels;
	}
	public int getIsleaf() {
		return isleaf;
	}
	public void setIsleaf(int isleaf) {
		this.isleaf = isleaf;
	}
	public int getIsenable() {
		return isenable;
	}
	public void setIsenable(int isenable) {
		this.isenable = isenable;
	}
	
	

}
