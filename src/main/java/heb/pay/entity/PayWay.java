package heb.pay.entity;

import java.util.Date;

public class PayWay {

	private String id;
	
	private int version;
	
	private String creator;
	
	private Date create_time;
	
	private String modificator;
	
	private Date modify_time;
	
	private String pay_product_code;
	
	private String status;
	
	private String pay_way_code;
	
	private String pay_way_name;
	
	private String pay_type_code;
	
	private String pay_type_name;
	
	private int pay_order;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getVersion() {
		return version;
	}

	public void setVersion(int version) {
		this.version = version;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

	public String getModificator() {
		return modificator;
	}

	public void setModificator(String modificator) {
		this.modificator = modificator;
	}

	public Date getModify_time() {
		return modify_time;
	}

	public void setModify_time(Date modify_time) {
		this.modify_time = modify_time;
	}

	public String getPay_product_code() {
		return pay_product_code;
	}

	public void setPay_product_code(String pay_product_code) {
		this.pay_product_code = pay_product_code;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getPay_way_code() {
		return pay_way_code;
	}

	public void setPay_way_code(String pay_way_code) {
		this.pay_way_code = pay_way_code;
	}

	public String getPay_way_name() {
		return pay_way_name;
	}

	public void setPay_way_name(String pay_way_name) {
		this.pay_way_name = pay_way_name;
	}

	public String getPay_type_code() {
		return pay_type_code;
	}

	public void setPay_type_code(String pay_type_code) {
		this.pay_type_code = pay_type_code;
	}

	public String getPay_type_name() {
		return pay_type_name;
	}

	public void setPay_type_name(String pay_type_name) {
		this.pay_type_name = pay_type_name;
	}

	public int getPay_order() {
		return pay_order;
	}

	public void setPay_order(int pay_order) {
		this.pay_order = pay_order;
	}
	
	

}
