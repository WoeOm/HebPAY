package heb.pay.entity;

public class JFBankInfo {

	private int bankClsId;
	
	private String bankClsCode;
	
	private String bankClsName;

	public int getBankClsId() {
		return bankClsId;
	}

	public void setBankClsId(int bankClsId) {
		this.bankClsId = bankClsId;
	}

	public String getBankClsCode() {
		return bankClsCode;
	}

	public void setBankClsCode(String bankClsCode) {
		this.bankClsCode = bankClsCode;
	}

	public String getBankClsName() {
		return bankClsName;
	}

	public void setBankClsName(String bankClsName) {
		this.bankClsName = bankClsName;
	}
	
	
}
